const day = "monday";

switch(day){
    case "monday":
        console.log("This is monday");
        break;
    case "tuesday":
        console.log("This is tuesday");
        break;
    default:
        console.log("not a valid day");
}

if(day === "monday"){
    console.log("This is monday");
}else if(day === "tuesday"){
    console.log("This is tuesday");
}else{
    console.log("not a valid day");
}

